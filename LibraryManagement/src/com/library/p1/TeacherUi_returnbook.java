    private void table_show()
    {
        String s1,s2;
        try {
                select = (PreparedStatement) con1.prepareStatement("select * from approved_books_for_teacher where mat_id=?");
                select.setString(1,matrix);
                ResultSet rs = select.executeQuery();
                DefaultTableModel df = (DefaultTableModel) jTable1_show_approved_books.getModel();
                df.setRowCount(0);
                while (rs.next()) 
                {
                    s1 = rs.getString("author");
                    s2 = rs.getString("books_name");
               
                    String[] row = { s1, s2 };
                    df.addRow(row);
            
                }
        } catch (SQLException ex) {
             System.out.println(ex);
        }
    }

    private void jButton2_return_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_return_bookActionPerformed
        // TODO add your handling code here:
        String book;
        book = jTextField1_books_name.getText();
        try{
            if(book.isEmpty()){
                JOptionPane.showMessageDialog(this,"please select a book first!");
            }
            else{
                delete = (PreparedStatement) con1.prepareStatement("delete from approved_books_for_teacher where books_name=?");
                delete.setString(1,book);
                delete.execute();
                jTextField1_books_name.setText("");
                JOptionPane.showMessageDialog(this,"book return successfull!");
            }
        }catch(SQLException ex){
            System.out.println(ex);
            
        }finally{
            table_show();
        }
    }//GEN-LAST:event_jButton2_return_bookActionPerformed

